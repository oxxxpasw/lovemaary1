-- Схема для Threads Wedding Chapel

-- 1. Таблица Пользователей
CREATE TABLE IF NOT EXISTS users (
  id UUID PRIMARY KEY DEFAULT auth.uid(),
  handle TEXT UNIQUE NOT NULL,
  avatar_url TEXT,
  silk INTEGER DEFAULT 100,
  role TEXT DEFAULT 'Моногам',
  status TEXT DEFAULT 'Свободен',
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 2. Таблица Предложений (Proposals)
CREATE TABLE IF NOT EXISTS proposals (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  from_handle TEXT REFERENCES users(handle) ON DELETE CASCADE,
  to_handle TEXT REFERENCES users(handle) ON DELETE CASCADE,
  status TEXT DEFAULT 'pending', -- pending, accepted, rejected
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 3. Таблица Браков (Marriages)
CREATE TABLE IF NOT EXISTS marriages (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  partner_a TEXT REFERENCES users(handle) ON DELETE CASCADE,
  partner_b TEXT REFERENCES users(handle) ON DELETE CASCADE,
  wedding_style TEXT DEFAULT 'Кибер-ЗАГС',
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Включаем Real-time для таблиц
ALTER PUBLICATION supabase_realtime ADD TABLE proposals;
ALTER PUBLICATION supabase_realtime ADD TABLE marriages;
ALTER PUBLICATION supabase_realtime ADD TABLE users;
